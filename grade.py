marks=int(input('Enter your mark for a subject: '))
if(marks>=90):
    print("your grade is 'S' ")
elif(marks>=80):
    print("Your grade is 'A' ")
elif(marks>=70):
    print("Your grade is 'B' ")
elif(marks>=60):
    print("Your grade is 'C' ")
elif(marks>=50):
    print("Your grade is 'D' ")
else:
    print("You have failed in the current subject! Good luck for next time.")